<H2>Backpropagation</H2>
<P>
Welcome to Backpropagation Android application. In this program you will be able set your own neural network and teach it some simple task by the well known algorithm of backpropagation. The network will learn your training set and hopefuly it will generalise some rules from it and use it on any input. The program is not designed to solve any complex problem from the real life like say image processing, speach recognition or weather forecast etc. It is now in the current version useful just for playing with it and learnig how the backpropagation works. If you problem is 2D -&gt; 3D or smaller, you will be able to draw a nice graph of you network result. Some network visualisation with each neuron and connection shown is also supported. The program supports import and export from human readable xml or csv and it has also a simple GUI editor.
</P>
